#include<stdio.h>
int main()
{
    int a=10,b=12;
    int add,sub,mul,div;
    add=a+b;
    sub=a-b;
    mul=a*b;
    div=a/b;
    printf("The Addition of a and b is:%d\n",add);
    printf("The Subtraction of a and b is:%d\n",sub);
    printf("The Multiplication of a and b is:%d\n",mul);
    printf("The Division of a and b is:%d\n",div);
    return 0;
}
